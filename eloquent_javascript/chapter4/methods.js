// string and array object methods
let name = "john";
console.log(name.toUpperCase()); // toUpperCase() is a method used to convert all characters in string to uppercase letters

let name2 = "JESS";
console.log(name2.toLowerCase()); // toLowerCase() is a method used to convert all characters in string to lowercase letters

let nameList = ["James", "Gaby", "Steve", "Bob"];
nameList.push("Harry", "Sally"); // push() is a method used to add items to the end of a array
console.log(nameList);

let nameList2 = ["James", "Gaby", "Steve", "Bob"];
nameList2.pop(); // pop() is a method used to remove the last item in an array from the array.
console.log(nameList2);

let numberList = [1, 3, 5, 7, 9, 11, 13];
console.log(numberList.length); // The length property or method returns the number of items in the variable numberList.
