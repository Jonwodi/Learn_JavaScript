// Array loops
// -------------------------------------------------------------------

// The for of loop is the equivalent of a python for loop
// the for of loop iterates over a item in an array one at a time
let animals = ["dog", "cat", "bear", "horse", "giraffe"]; // a variable called animals that contains a array of string values

for (let animal of animals) {
  // for every animal in the array variable called animals peform the console.log defined in the for of loop body or block
  console.log(`My favourite animal is a ${animal}`);
}
