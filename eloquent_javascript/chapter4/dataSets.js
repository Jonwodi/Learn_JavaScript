// arrays
let numberList = [1, 3, 5, 7, 9, 11, 13]; // a array is a list values betwween square brackets, seperated by commas
console.log(numberList); // returns a array of number values assigned to the variable numberList

// The first item in an array always has an index position of 0.
console.log(numberList[0]); // returns the number value 1 because this is the first item in the array
console.log(numberList[6]); // returns the number value 13 because this value has an index position of 6 in the array
console.log(numberList[6 - 3]); // finds the item that has index position of 6 and goes back by 3 index postion. So the number value of 7 is returned.
