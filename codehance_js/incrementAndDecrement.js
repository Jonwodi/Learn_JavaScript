// incrementing
let i = 5;
i++;
i++;
console.log(i);

let y = 3;
console.log(++y);

let j = 10;
console.log(j++);
console.log(j);

let a = 20;
a +=2;
console.log(a);

// Decrementing
let n = 10;
n--;
n--;
n--;
console.log(n);

let z = 4;
console.log(--z);

let k = 15;
console.log(k--);
console.log(k);

